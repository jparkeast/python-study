print("Welcome to Treasure Island.")
print("Your mission is to find the treasure.")

game = True

while game:
    print("\n" + "*" * 40)
    print("입구에 서 있습니다. 어디로 가시겠습니까?")
    paths = input("1.(숲), 2.(동굴) ").lower()

    if paths == "2":
        print("동굴 속에 있던 곰을 만났습니다! GAME OVER")
        # 여기서 재시도를 쓰면 다시 맨 처음 '입구'로 돌아갑니다.
        retry = input("다시 도전하시겠습니까? (y/n): ")
        if retry != "y":
            break
        continue

    # ---choice2
    print("\n해변에 도착했습니다. 부둣가에 배가 보입니다.")
    choice2 = input("1. 기다린다, 2. 헤엄쳐서 간다, 3. 캠핑을 한다: ")

    if choice2 == "2":
        print("상어에게 습격을 당했습니다! GAME OVER")
        continue  # 처음으로 돌아감

    elif choice2 == "3":
        # 확률 요소 추가
        luck = random.randint(1, 2)
        if luck == 1:
            print("밤사이에 뱀에게 물렸습니다. GAME OVER")
            continue
        else:
            print("운 좋게 밤을 잘 보냈습니다!")

    # ---choice3
    print("\n드디어 세 개의 문 앞에 섰습니다.")
    door = input("어떤 문을 열까요? 1.빨강, 2.파랑, 3.노랑: ")

    if door == "3":
        print("\n✨ 축하합니다! 보물을 찾았습니다! 당신이 승리했습니다! ✨")
        game = False  # 게임 종료
    elif door == "1":
        print("불구덩이에 빠졌습니다! GAME OVER")
    else:
        print("괴물이 살고 있는 방입니다! GAME OVER")